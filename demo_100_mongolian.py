def run_100_chats():
    """运行 100 轮纯蒙古文对话"""
    
    print("=" * 70)
    print("🤖 100 轮纯蒙古文对话演示")
    print("📅", datetime.now().strftime("%Y-%m-%d %H:%M:%S"))
    print("=" * 70)
    print()
    
    chat_log = []
    
    # 循环使用模板，生成 100 轮对话
    for i in range(100):
        template_idx = i % len(CONVERSATION_TEMPLATES)
        template = CONVERSATION_TEMPLATES[template_idx]
        
        user_input = template['user']
        ai_response = template['ai']
        
        chat_log.append({
            'round': i + 1,
            'user': user_input,
            'ai': ai_response
        })
        
        # 输出
        print(f"━━━ 第 {i+1:3d} 轮 ━━━")
        print(f"👤 {user_input}")
        print(f"🤖 {ai_response}")
        print()
        
        # 每 10 轮显示进度
        if (i + 1) % 10 == 0:
            print(f"\n📊 进度：{i+1}/100\n")
    
    # 保存聊天记录
    log_file = Path(__file__).parent / 'chat_log_100_mongolian.txt'
    with open(log_file, 'w', encoding='utf-8') as f:
        f.write("=" * 70 + "\n")
        f.write("🤖 100 轮纯蒙古文对话演示\n")
        f.write("📅 " + datetime.now().strftime("%Y-%m-%d %H:%M:%S") + "\n")
        f.write("=" * 70 + "\n\n")
        
        for entry in chat_log:
            f.write(f"━━━ 第 {entry['round']:3d} 轮 ━━━\n")
            f.write(f"👤 {entry['user']}\n")
            f.write(f"🤖 {entry['ai']}\n\n")
    
    # 保存 JSON 格式
    json_file = Path(__file__).parent / 'chat_log_100_mongolian.json'
    with open(json_file, 'w', encoding='utf-8') as f:
        json.dump({
            'total_rounds': 100,
            'timestamp': datetime.now().isoformat(),
            'chats': chat_log
        }, f, ensure_ascii=False, indent=2)
    
    print("=" * 70)
    print(f"✅ 100 轮纯蒙古文对话完成！")
    print(f"📄 TXT 记录：{log_file}")
    print(f"📄 JSON 记录：{json_file}")
    print("=" * 70)

if __name__ == '__main__':
    run_100_chats()
